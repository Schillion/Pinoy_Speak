"use client";

import { motion } from "framer-motion";
import { usePathname } from "next/navigation";

export default function PageTransition({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();
  // Animate in on every route change. We deliberately avoid AnimatePresence /
  // exit animations here — Next's App Router swaps `children` synchronously
  // during navigation, which races AnimatePresence and can leave the old
  // page mounted indefinitely (forcing a refresh to recover).
  return (
    <motion.div
      key={pathname}
      initial={{ opacity: 0, y: 8 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.28, ease: [0.22, 1, 0.36, 1] }}
    >
      {children}
    </motion.div>
  );
}
